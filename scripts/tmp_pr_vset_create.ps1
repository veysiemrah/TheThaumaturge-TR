        $lines += "`"$($entry.Hash)`",`"$node`",`"$pl`",`"$tr`",`"`""
        $totalLines++
    }
    $outPath = Join-Path "translation" $file.File
    Set-Content -Path $outPath -Value $lines -Encoding UTF8
}
Write-Host "Yazıldı: $($rows.Count) dosya, $totalLines satır"
